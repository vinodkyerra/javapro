
public class ifelseif {

	public static void main(String[] args) {
		int x=10;
		if (x==2)
				{
			System.out.println("x is 2" );
				}
		else if (x==3) {
			System.out.println("x is 3");
		}else if (x==4) {
			System.out.println("x is 4");
		}else {
			System.out.println("this is learning process");
		}
	}

}

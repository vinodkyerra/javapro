
public class loopsfor {

	public static void main(String[] args)
	{
		for (int i=0;i<5;i=i+1) {
			
		{
			for (int j=0;j<=i;j++);
			{
			System.out.println("");
			}
				
			for (int k=1; k>=i; k++)
			{
			System.out.print( k +"" );	
			}
			
			System.out.println("");
			
			}
		
	}

	}
}
	

public class loopsfor {

	public static void main(String[] args)
	{
		for (int i=1;i<5;i=i+1)
		{
			for (int k=3;k>=i;k--);
			{
				System.out.println(" ");
			}
				
			for (int j=1; j<=i; j++)
			{
			
			System.out.print( j +" " );
				
			}
			System.out.println(" ");
			
			}
		
	}

	}
	